#include <stdio.h>

int main() {
  int a = 125;   // numero em base decimal
  int b = 0435;  // numero em base octal
  int c = 0x1FA; // numero em base hexadecimal

  printf("Valor de a: %d - base decimal\n", a);
  printf("Valor de a: %d - base octal\n", b);
  printf("Valor de a: %d - base hexadecimal\n", c);

  return 0;
}
